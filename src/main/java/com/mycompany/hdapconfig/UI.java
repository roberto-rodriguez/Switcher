    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        if (tabla.getSelectedRow() == -1) {
            return;
        } else {
            selectedIndex = tabla.getSelectedRow();

            String name = (String) tabla.getModel().getValueAt(selectedIndex, 0);
            String value = (String) tabla.getModel().getValueAt(selectedIndex, 1);

            new Form(this, name, value).setVisible(true);
        }
    }//GEN-LAST:event_jLabel2MouseClicked

    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        if (tabla.getSelectedRow() == -1) {
            return;
        } else {
            int selection = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete?");

            if (selection == 0) {
                try {
                    tabla.setModel(Main.deleteHost((DefaultTableModel) tabla.getModel(), tabla.getSelectedRow()));
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jLabel3MouseClicked

