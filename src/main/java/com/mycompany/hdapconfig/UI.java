    private void switchHost() throws Exception {     
        if(!validSettings){
            new Settings(Main.getSettings(), this).setVisible(true);
        }else{
            int index = tabla.getSelectedRow();

        if (index == -1) {
            throw new Exception("Selection Required");
        }

        String hostName = (String) tabla.getModel().getValueAt(index, 0);
        String hostValue = (String) tabla.getModel().getValueAt(index, 1);

        String currentHost = Main.getCURRENT_HOST();

        Main.switchHost(hostName, hostValue);

        result.setText("Environment switched from " + currentHost + " to " + hostName + ".");
        currentHostLabel.setText("Current Environment: " + hostName);
        }
    }

